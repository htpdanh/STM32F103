void HAL_UART_RxCpltCallback(UART_HandleTypeDef *huart)
{
  Rx_Array[Rx_Index]=Rx_Data;
	Rx_Index++;
	if (Rx_Data==10) {
		for(int i=(Rx_Index-1);i<20;i++) {
			Rx_Array[i]=0;
		}
		Rx_Index=0;
	}
	HAL_UART_Receive_IT(&huart1,&Rx_Data,1);
}

void PWM_Output(uint8_t servo_duty)
{
	__HAL_TIM_SetCompare(&htim2,TIM_CHANNEL_1,servo_duty);
}

void LED_Output(GPIO_PinState PinState)
{
	HAL_GPIO_WritePin(GPIOC,GPIO_PIN_13,PinState);
}

void Repeat_Check(){
	if (strcmp((const char*)Rx_Array,"Repeat=ON") == 0)
	{
		CheckMode=1;
		memset((char*)Rx_Array,0,20);
	}
	else if (strcmp((const char*)Rx_Array,"Repeat=OFF") == 0)
	{
		CheckMode=0;
	}
}

void Repeat_Mode(){
	HAL_UART_Transmit(&huart1,Rx_Array,20,HAL_MAX_DELAY);
	HAL_Delay(1000);
}


void Command_Mode(){
	if (strcmp((const char*)Rx_Array,LED_ON) == 0)
	{
			LED_Output(GPIO_PIN_RESET);
	}
	if (strcmp((const char*)Rx_Array,LED_OFF) == 0)
	{
			LED_Output(GPIO_PIN_SET);
	}
	if (strcmp((const char*)Rx_Array,SERVO_90) == 0)
	{
		//PWM = 20/200(ms) corresponds to angle of servo is 90.
			PWM_Output(20);
	}
}

