#define START_TIME_MAX 140
#define START_TIME_MIN 120
#define COMMAND_0  15
/* define Button for remote*/
#define BUTTON_0 0x98
#define BUTTON_1 0xA2
#define BUTTON_2 0x62
#define BUTTON_3 0xE2
#define BUTTON_4 0x22
#define BUTTON_5 0x02

uint16_t Time1 = 0;
uint16_t Time2 = 0;
uint16_t Period = 0;

uint8_t counter_hex = 0; 
uint8_t command = 0;
uint32_t hex_ir = 0;


void ControlPWM(uint16_t Duty){
	__HAL_TIM_SetCompare(&htim3,TIM_CHANNEL_1,Duty);
}

void ControlOutput(void){
	static uint16_t LedDuty;
	switch(command){
				case BUTTON_0:	//button 0
					LedDuty=0;
					break;
				case BUTTON_1:	//button 1
					LedDuty=400;
					break;
				case BUTTON_2:	//button 2
					LedDuty=800;
					break;	
				case BUTTON_3:	//button 3
					LedDuty=1200;
					break;
				case BUTTON_4:	//button 4
					LedDuty=1600;
					break;
				case BUTTON_5:	//button 5
					LedDuty=2000;
					break;
			}
	ControlPWM(LedDuty);
}

void HAL_TIM_IC_CaptureCallback(TIM_HandleTypeDef *htim)
{ 
		Time2 = HAL_TIM_ReadCapturedValue(htim, TIM_CHANNEL_1);
		Period = Time2 - Time1;
		Time1 = Time2;
		
		//kiem tra xung
		if(Period < START_TIME_MAX && Period > START_TIME_MIN)
		{	
			hex_ir = 0;
			counter_hex = 0;
		}
		else if(Period > COMMAND_0) //bit 1
		{
			hex_ir |=(1<<(31-counter_hex));
			counter_hex++;
		}
		else if(Period < COMMAND_0) //bit 0
		{
			counter_hex++;
		}		
		
		if(counter_hex == 32) //ghi 32 lan
		{
			command = hex_ir>>8;
			ControlOutput();
		}
		
}
